class SwitchTest
{
	public static void main(String [] args)
	{
		switch(3)
		{
			case 0 : 
			System.out.print("zero ");
			break;
			case 1 :
			System.out.print("one ");
			break;
			default :
			System.out.print("default ");
			break;
			case 2 :
			System.out.print("two\n");	
		}
	}
}
